// assistantEngine.js — Matching engine for the AI Playground Assistant
import { PLAYGROUND_KB, FOLLOWUPS } from './AssistantKB.js'

export function generateAssistantResponse(message, lang = 'en') {
  const lower = message.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '') // remove accents
    .trim()

  // Greetings
  if (/^(hola|hello|hi|hey|buenos|buenas|que tal|good morning|good afternoon|saludos|sup|yo)/.test(lower)) {
    return greeting(lang)
  }

  // Thanks
  if (/^(thanks|gracias|thank you|merci|thx|ty)/.test(lower)) {
    return thanks(lang)
  }

  // Help
  if (/^(help|ayuda|menu|opciones|que puedo preguntar|commands|comandos|\?)/.test(lower)) {
    return helpMenu(lang)
  }

  // Surprise/random question
  if (/^(surprise|sorpresa|random|algo interesante|fun fact|dato curioso|sorprendeme|surprise me)/.test(lower)) {
    return randomTopic(lang)
  }

  // Score all topics
  const scores = Object.entries(PLAYGROUND_KB).map(([key, topic]) => {
    let score = 0
    for (const pattern of topic.question_patterns) {
      const patLower = pattern.toLowerCase()
        .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
      if (lower.includes(patLower)) {
        score += patLower.split(' ').length * 2
      }
      // Also check individual word overlap for partial matches
      const patWords = patLower.split(/\s+/)
      for (const w of patWords) {
        if (w.length > 3 && lower.includes(w)) {
          score += 1
        }
      }
    }
    return { key, topic, score }
  }).filter(s => s.score > 0).sort((a, b) => b.score - a.score)

  if (scores.length > 0) {
    const best = scores[0]
    return {
      text: lang === 'es' ? best.topic.answer_es : best.topic.answer_en,
      topic: best.key,
      confidence: Math.min(0.95, best.score * 0.15),
      suggestedFollowups: getFollowups(best.key, lang),
      mode: 'knowledge'
    }
  }

  // Agentic mode for open-ended questions
  if (lower.length > 30) {
    return agenticResponse(message, lang)
  }

  return fallback(lang)
}

function agenticResponse(message, lang) {
  const lower = message.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')

  const hasWhy = /why|por que|porque/.test(lower)
  const hasHow = /how|como|de que manera/.test(lower)
  const hasCompare = /compare|vs|versus|diferencia|mejor/.test(lower)

  let reasoning = ''
  if (hasWhy) {
    reasoning = lang === 'es'
      ? '**Modo Agente — Razonamiento profundo:**\n\nAnalizando tu pregunta "por que"...\n\n'
      : '**Agentic Mode — Deep Reasoning:**\n\nAnalyzing your "why" question...\n\n'
  } else if (hasHow) {
    reasoning = lang === 'es'
      ? '**Modo Agente — Analisis paso a paso:**\n\n'
      : '**Agentic Mode — Step-by-step Analysis:**\n\n'
  } else if (hasCompare) {
    reasoning = lang === 'es'
      ? '**Modo Agente — Analisis comparativo:**\n\n'
      : '**Agentic Mode — Comparative Analysis:**\n\n'
  } else {
    reasoning = lang === 'es'
      ? '**Modo Agente — Exploracion abierta:**\n\n'
      : '**Agentic Mode — Open Exploration:**\n\n'
  }

  // Search KB for related topics and synthesize
  const related = findRelatedTopics(message, 3)
  if (related.length > 0) {
    const context = related.map(r => lang === 'es' ? r.topic.answer_es : r.topic.answer_en).join('\n\n')
    reasoning += context
    reasoning += lang === 'es'
      ? '\n\n---\n*Respuesta generada combinando conocimiento de multiples temas.*'
      : '\n\n---\n*Response generated by combining knowledge from multiple topics.*'
  } else {
    reasoning += lang === 'es'
      ? 'Esta es una pregunta interesante que va mas alla de los temas predefinidos. En un entorno de produccion, esto se procesaria con un LLM como Llama 3.3 70B para darte una respuesta completa y contextualizada.'
      : 'This is an interesting question that goes beyond predefined topics. In production, this would be processed by an LLM like Llama 3.3 70B for a complete, contextualized response.'
  }

  return {
    text: reasoning,
    topic: 'agentic',
    confidence: 0.7,
    suggestedFollowups: lang === 'es'
      ? ['Que mas puedo preguntar?', 'Ayuda', 'Sorpresa']
      : ['What else can I ask?', 'Help', 'Surprise'],
    mode: 'agentic'
  }
}

function findRelatedTopics(message, topK) {
  const lower = message.toLowerCase()
    .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
  return Object.entries(PLAYGROUND_KB)
    .map(([key, topic]) => {
      let score = 0
      for (const p of topic.question_patterns) {
        const words = p.toLowerCase()
          .normalize('NFD').replace(/[\u0300-\u036f]/g, '')
          .split(' ')
        for (const w of words) {
          if (w.length > 3 && lower.includes(w)) score++
        }
      }
      return { key, topic, score }
    })
    .filter(s => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
}

