function randomTopic(lang) {
  const keys = Object.keys(PLAYGROUND_KB)
  const funKeys = ['easter_egg', 'best_feature', 'compare_playground_vs_chatgpt', 'future_features', 'groq_api', 'performance', 'use_cases']
  const pool = funKeys.filter(k => keys.includes(k))
  const key = pool[Math.floor(Math.random() * pool.length)]
  const topic = PLAYGROUND_KB[key]
  return {
    text: lang === 'es' ? topic.answer_es : topic.answer_en,
    topic: key,
    confidence: 0.9,
    suggestedFollowups: getFollowups(key, lang),
    mode: 'knowledge'
  }
}

function fallback(lang) {
  return {
    text: lang === 'es'
      ? 'No estoy seguro de entender tu pregunta. Intenta preguntar sobre alguna funcion especifica como "Que es el analisis?", "Como funciona RAG?", o "Comparar modelos". Escribe **ayuda** para ver todos los temas disponibles.'
      : 'I\'m not sure I understand your question. Try asking about a specific feature like "What is analysis?", "How does RAG work?", or "Compare models". Type **help** to see all available topics.',
    topic: 'fallback',
    confidence: 0,
    suggestedFollowups: lang === 'es'
      ? ['Ayuda', 'Que es esto?', 'Funciones', 'Sorpresa']
      : ['Help', 'What is this?', 'Features', 'Surprise'],
    mode: 'knowledge'
  }
}

function getFollowups(topicKey, lang) {
  const fu = FOLLOWUPS[topicKey]
  if (fu) return lang === 'es' ? fu.es : fu.en
  return lang === 'es'
    ? ['Ayuda', 'Todas las funciones', 'Sorpresa']
    : ['Help', 'All features', 'Surprise']
}
